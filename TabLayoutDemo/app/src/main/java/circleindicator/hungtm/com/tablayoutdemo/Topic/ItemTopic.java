package circleindicator.hungtm.com.tablayoutdemo.Topic;

public class ItemTopic {
    private int idImageTopic;
    private String tvTopic;

    public ItemTopic(int idImageTopic, String tvTopic) {
        this.idImageTopic = idImageTopic;
        this.tvTopic = tvTopic;
    }

    public int getIdImageTopic() {
        return idImageTopic;
    }

    public void setIdImageTopic(int idImageTopic) {
        this.idImageTopic = idImageTopic;
    }

    public String getTvTopic() {
        return tvTopic;
    }

    public void setTvTopic(String tvTopic) {
        this.tvTopic = tvTopic;
    }
}
